package chapter5;

public class Dish {
    private String name;
    private int price;
    private int calories;
    private boolean vegetarian;

    public String getName() {
        return name;
    }

    public int getPrice() {
        return price;
    }

    public int getCalories() {
        return calories;
    }

    public boolean isVegetarian() {
        return vegetarian;
    }
}
