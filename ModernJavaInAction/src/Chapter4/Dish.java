package Chapter4;

public class Dish {

    private String name;
    private int calories;
    private String type;
    private boolean isVegetarian;

    public Boolean isVegetarian() {
        return isVegetarian;
    }

    public String getName() {
        return name;
    }

    public Integer getCalories() {
        return calories;
    }

    public String getType() {
        return type;
    }
}
